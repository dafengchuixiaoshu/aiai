- (void)mergeVideoFiles
{
    NSMutableArray *fileURLArray = [[NSMutableArray alloc] init];
    for (SBVideoData *data in _videoFileDataArray) {
        [fileURLArray addObject:data.fileURL];
    }
    
    [SBVideoRecorder mergeAndExportVideosAtFileURLs:fileURLArray withScale:1 withCompleteBlock:^(NSURL *path, CGFloat length) {
        
        dispatch_async(dispatch_get_main_queue(), ^{
            if ([_delegate respondsToSelector:@selector(videoRecorder:didFinishMergingVideosToOutPutFileAtURL: totalDur:)]) {
                [_delegate videoRecorder:self didFinishMergingVideosToOutPutFileAtURL:path totalDur:length];
            }
        });
        
    }];
}

