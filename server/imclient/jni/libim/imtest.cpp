string GetRootDirectory ()
{
	string root;
	char *buffer;
	if((buffer = getcwd(NULL, 0)) == NULL)
	{
		printf ("getcwd error");
		return "";
	}
	else
	{
		printf ("%s\n", buffer);
		root = buffer;
		free(buffer);
	}
	return root;
}

